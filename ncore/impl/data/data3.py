class ShardDataLoader:
    """ShardDataLoader providing convenience methods to load data generated by data-converters"""

    @staticmethod
    def evaluate_shard_file_pattern(pattern: str, skip_suffixes: Iterable[str] = ()) -> List[str]:
        """Given a shard-file-pattern returns a list of matching and existing files

        Supported patterns (mutually exclusive):
        - integer-ranges: '/some/path/file-[1-3]' will be expanded to [/some/path/file-1, /some/path/file-2, /some/path/file-3]

        """

        pattern_basepath = Path(pattern).parent
        pattern_name = Path(pattern).name

        evaluated_name_patterns = []

        # expand integer ranges like '[1-13]'
        if range_match := re.search(r"\[(\d+)-(\d+)\]", pattern_name):
            low = int(range_match.group(1))
            high = int(range_match.group(2))

            for i in range(low, high + 1):
                evaluated_name_patterns.append(pattern_name.replace(f"[{low}-{high}]", str(i) + "-"))
        else:
            evaluated_name_patterns.append(pattern_name)

        matches: set[Path] = set()
        for evaluated_pattern in evaluated_name_patterns:
            for candidate in pattern_basepath.iterdir():
                if candidate.name.startswith(evaluated_pattern):
                    skip = False
                    for skip_suffix in skip_suffixes:
                        if str(candidate).endswith(skip_suffix):
                            skip = True
                            break
                    if not skip:
                        matches.add(candidate)

        return [str(match) for match in list(matches)]

    def __init__(self, shard_paths: List[str], open_consolidated: bool = True):
        """Initialize a ShardDataLoader for a virtual sequence represented by a list of shard files.

        Args:
            shard_files: Paths / URLs to shard files to load, which need to represent a *continuous* sequence
            open_consolidated: If 'True', pre-load per-shard meta-data when opening the shards.
                               This is advisable if shard data is accessed from *non-local*
                               storage to prevent latencies introduced when accessing the data.
                               If the shard data is available on fast *local* storage, disabling
                               this option can speed up initial load times.
        """

        assert len(shard_paths), "No shard inputs provided"

        # Load shards concurrently (to hide latency) and check for sequence consistency and continuity of shards
        shards_map: Dict[
            int, Tuple[str, zarr.Group, stores.IndexedTarStore]
        ] = {}  # use str as the generic path / URL type

        with concurrent.futures.ThreadPoolExecutor() as executor:

            def thread_load_shard(shard_path):
                """Thread-executed shard opening"""

                # Make sure paths are absolute at this point - in the future we might have fully-resolved URLs instead here, too
                shard_path = Path(shard_path).absolute()

                logging.info(f"ShardDataLoader: Loading shard {shard_path}")

                timer = common.SimpleTimer()
                shard_store = stores.IndexedTarStore(shard_path)
                shard_root = (
                    stores.open_compressed_consolidated(store=shard_store, mode="r")
                    if open_consolidated
                    else zarr.open(store=shard_store, mode="r")
                )

                logging.debug(f"ShardDataLoader: {shard_path} time_load={timer.elapsed_sec()}sec")

                return str(shard_path), shard_root, shard_store

            for future in concurrent.futures.as_completed(
                [executor.submit(thread_load_shard, shard_path) for shard_path in shard_paths]
            ):
                # Note: thread completion order is not relevant here
                shard_path, shard_root, shard_store = future.result()

                shard_sequence_id = shard_root.attrs.get("sequence_id")
                shard_camera_ids = set(shard_root.attrs.get("camera_ids"))
                shard_lidar_ids = set(shard_root.attrs.get("lidar_ids"))
                shard_radar_ids = set(shard_root.attrs.get("radar_ids"))
                shard_shard_id = shard_root.attrs.get("shard_id")
                shard_shard_count = shard_root.attrs.get("shard_count")
                shard_version = shard_root.attrs.get("version")
                shard_calibration_type = shard_root.attrs.get("calibration_type")
                shard_egomotion_type = shard_root.attrs.get("egomotion_type")

                if not shards_map:
                    self._sequence_id: str = shard_sequence_id
                    self._camera_ids: set[str] = shard_camera_ids
                    self._lidar_ids: set[str] = shard_lidar_ids
                    self._radar_ids: set[str] = shard_radar_ids
                    self._shard_count: int = shard_shard_count
                    self._shard_version: str = shard_version
                    self._calibration_type: str = shard_calibration_type
                    self._egomotion_type: str = shard_egomotion_type

                if not self._sequence_id == shard_sequence_id:
                    raise ValueError("Can't load shards from different sequences")
                if not self._camera_ids == shard_camera_ids:
                    raise ValueError("Can't load shards with different camera sensors")
                if not self._lidar_ids == shard_lidar_ids:
                    raise ValueError("Can't load shards with different lidar sensors")
                if not self._radar_ids == shard_radar_ids:
                    raise ValueError("Can't load shards with different radar sensors")
                if not self._shard_count == shard_shard_count:
                    raise ValueError("Can't load shards from different subdivisions")
                if not self._shard_version == shard_version:
                    raise ValueError("Can't load shards from different data versions")
                if not self._calibration_type == shard_calibration_type:
                    raise ValueError("Can't load shards with different calibration types")
                if not self._egomotion_type == shard_egomotion_type:
                    raise ValueError("Can't load shards with different egomotion types")

                if shard_shard_id in shards_map:
                    raise ValueError("Shard ID loaded multiple times")

                shards_map[shard_shard_id] = shard_path, shard_root, shard_store

        # Check version-compatibility
        if self._shard_version != VERSION:
            raise ValueError(
                f"Loading incompatible version {self._shard_version}, supporting {VERSION} only"
            )  # TODO: this check can still be refined

        # Make sure shard IDs are continuous
        self._shard_ids = sorted(list(shards_map.keys()))
        if self._shard_ids[-1] - self._shard_ids[0] + 1 != len(self._shard_ids):
            raise ValueError(f"Loading non-continuous sequence of shards: {self._shard_ids}")

        # *Linear* sequences of shards
        self._shard_paths = [shards_map[shard_id][0] for shard_id in self._shard_ids]
        self._shard_roots = [shards_map[shard_id][1] for shard_id in self._shard_ids]
        self._shard_stores = [shards_map[shard_id][2] for shard_id in self._shard_ids]

    def reload_store_resources(self) -> None:
        """Trigger a reload of each shard store - useful to re-initialize file objects in multi-process settings"""
        for shard_store in self._shard_stores:
            shard_store.reload_resources()

    def get_poses(self, start_shard_idx: Optional[int] = None, stop_shard_idx: Optional[int] = None) -> types.Poses:
        """Returns all timestamped poses associated with the session (default) or a range of shards [start,stop)"""

        # Load common base pose
        # [TODO(janickm): add consistency check on static data across all shards?]
        T_rig_world_base = self._shard_roots[0]["poses"]["T_rig_world_base"]

        # Concat all poses from all shards, making sure they are uniquely timestamped and sorted
        T_rig_worlds = []
        T_rig_world_timestamps_us = []
        for shard_root in self._shard_roots[start_shard_idx:stop_shard_idx]:
            shard_T_rig_worlds = shard_root["poses"]["T_rig_worlds"][()]
            shard_T_rig_world_timestamps_us = shard_root["poses"]["T_rig_world_timestamps_us"][()]

            if len(shard_T_rig_worlds) != len(shard_T_rig_world_timestamps_us):
                raise ValueError(f"Inconsistent pose number / timestamp number for shard: {shard_root}")

            if not np.all(shard_T_rig_world_timestamps_us[:-1] < shard_T_rig_world_timestamps_us[1:]):
                raise ValueError(f"Pose timestamps not strictly monotonically increasing for shard: {shard_root}")

            T_rig_worlds.append(shard_T_rig_worlds)
            T_rig_world_timestamps_us.append(shard_T_rig_world_timestamps_us)

        # Shards are allowed to have *single* overlapping poses across their boundary - remove these via unique search -
        # and make sure that pose timestamps are ordered in strictly monotonically increasing order afterwards
        T_rig_world_timestamps_us, unique_idxs, unique_counts = np.unique(
            np.hstack(T_rig_world_timestamps_us), return_index=True, return_counts=True
        )

        # Verify that overlapping poses showed up duplicated only once after concatenation
        if not np.all(unique_counts <= 2):
            raise ValueError(f"Concatenated pose timestamps contain more than a single overlapping common pose")

        # Make sure that concatenated timestamps were actually already sorted before unification
        if not np.all(unique_idxs[:-1] < unique_idxs[1:]):
            raise ValueError(f"Concatenated pose timestamps not strictly monotonically increasing")

        return types.Poses(
            np.array(T_rig_world_base), np.vstack(T_rig_worlds)[unique_idxs], np.hstack(T_rig_world_timestamps_us)
        )

    def get_sequence_id(self, with_shard_range: bool = False) -> str:
        """Provides access to a unique identifier of the loaded shard data, optionally including the linear range of shards

        Examples:
        - with_shard_range == False: c9b05cf4-afb9-11ec-b3c2-00044bf65fcb
        - with_shard_range == True:  c9b05cf4-afb9-11ec-b3c2-00044bf65fcb_2_3_4 [assuming shards 2,3,4 were loaded]
        """
        if with_shard_range:
            return f"{self._sequence_id}_{'_'.join([str(shard_id) for shard_id in self._shard_ids])}"
        return self._sequence_id

    def get_calibration_type(self) -> str:
        """Provides access to a calibration type identifier of the loaded shard data"""
        return self._calibration_type

    def get_egomotion_type(self) -> str:
        """Provides access to a egomotion type identifier of the loaded shard data"""
        return self._egomotion_type

    def get_shard_paths(self) -> List[str]:
        """Returns paths to loaded shards (ordered by linear shard ID)"""
        return self._shard_paths

    def get_shard_ids(self) -> List[int]:
        """Returns linearly ordered list of loaded shard IDs"""
        return self._shard_ids

    def get_sensor(self, sensor_id: str) -> Union[CameraSensor, LidarSensor, RadarSensor]:
        """Provides access to a specific sensor given its sensor-id"""
        if sensor_id in self._camera_ids:
            return self.get_camera_sensor(sensor_id)
        if sensor_id in self._lidar_ids:
            return self.get_lidar_sensor(sensor_id)
        if sensor_id in self._radar_ids:
            return self.get_radar_sensor(sensor_id)
        raise ValueError(f"Unknown sensor {sensor_id}")

    def get_sensor_ids(self) -> List[str]:
        """Returns all sensor ids"""
        return self.get_camera_ids() + self.get_lidar_ids() + self.get_radar_ids()

    def get_camera_sensor(self, camera_id) -> CameraSensor:
        """Provides access to a specific camera sensor given its sensor-id"""
        return CameraSensor(camera_id, CAMERAS_BASE_GROUP, self._shard_roots)

    def get_camera_ids(self) -> List[str]:
        """Returns all camera sensor ids"""
        return list(self._camera_ids)

    def get_lidar_sensor(self, lidar_id) -> LidarSensor:
        """Provides access to a specific lidar sensor given its sensor-id"""
        return LidarSensor(lidar_id, LIDARS_BASE_GROUP, self._shard_roots)

    def get_lidar_ids(self) -> List[str]:
        """Returns all lidar sensor ids"""
        return list(self._lidar_ids)

    def get_radar_sensor(self, radar_id) -> RadarSensor:
        """Provides access to a specific radar sensor given its sensor-id"""
        return RadarSensor(radar_id, RADARS_BASE_GROUP, self._shard_roots)

    def get_radar_ids(self) -> List[str]:
        """Returns all radar sensor ids"""
        return list(self._radar_ids)

    def get_tracks(self) -> types.Tracks:
        """Returns all object tracks"""

        root_shard = self._shard_roots[0]  # can use first root shard as track labels are the same per shard

        # Backwards-compatibility: track-labels used to be stored in top-level
        # 'labels' group / 'track_labels' dataset
        tracks_group = root_shard["labels"] if "labels" in root_shard else root_shard["tracks"]

        return types.Tracks(
            track_labels={
                track_label[0]: types.TrackLabel.from_dict(track_label[1], infer_missing=True)
                for track_label in tracks_group["track_labels"]
            }
        )
