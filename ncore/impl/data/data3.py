VERSION = "3.0.0"
SENSORS_BASE_GROUP = "sensors"
CAMERAS_BASE_GROUP = "cameras"
LIDARS_BASE_GROUP = "lidars"
RADARS_BASE_GROUP = "radars"

JsonLike = Union[Dict[str, "JsonLike"], List["JsonLike"], str, int, float, bool, None]


class ContainerDataWriter:
    """DataWriter implementing format-specific serialization specifications to store V3 / zarr data generated by data-converters"""

    def __init__(
        self,
        output_dir_path: Path,
        container_name: str,
        camera_ids: List[str],
        lidar_ids: List[str],
        radar_ids: List[str],
        calibration_type: str,
        egomotion_type: str,
        sequence_id: str,
        shard_id: int,
        shard_count: int,
        store_shard_meta: bool,
    ):
        """
        Instantiate data writer and initialize the default data groups for a given sequence and sensor IDs
        """

        self.camera_ids = camera_ids
        self.lidar_ids = lidar_ids
        self.radar_ids = radar_ids

        # Check that sensor-id's are unique across sensor types
        assert len(set(self.camera_ids + self.lidar_ids + self.radar_ids)) == len(self.camera_ids) + len(
            self.lidar_ids
        ) + len(self.radar_ids), "sensor id's are not unique"

        self.output_dir_path = output_dir_path
        self.container_name = container_name
        self.output_container_path = self.output_dir_path / f"{self.container_name}.zarr.itar"

        self.sequence_id = sequence_id
        self.shard_id = shard_id
        self.shard_count = shard_count

        self.store_shard_meta = store_shard_meta

        # Initialize container file (indexed tar file)
        self.output_dir_path.mkdir(parents=True, exist_ok=True)
        self.container_store = stores.IndexedTarStore(self.output_container_path, mode="w")
        self.container_root = zarr.group(store=self.container_store)

        # Store dataset associated meta-data
        self.container_root.attrs.put(
            {
                "version": VERSION,
                "egomotion_type": egomotion_type,
                "calibration_type": calibration_type,
                "camera_ids": self.camera_ids,
                "lidar_ids": self.lidar_ids,
                "radar_ids": self.radar_ids,
                "sequence_id": self.sequence_id,
                "shard_id": self.shard_id,
                "shard_count": self.shard_count,
            }
        )

        # Create sensor groups
        sensors_grp = self.container_root.require_group(SENSORS_BASE_GROUP)
        cameras_grp = sensors_grp.require_group(CAMERAS_BASE_GROUP)
        for camera_id in self.camera_ids:
            cameras_grp.require_group(camera_id)

        lidars_grp = sensors_grp.require_group(LIDARS_BASE_GROUP)
        for lidar_id in self.lidar_ids:
            lidars_grp.require_group(lidar_id)

        radars_grp = sensors_grp.require_group(RADARS_BASE_GROUP)
        for radar_id in self.radar_ids:
            radars_grp.require_group(radar_id)

        # Store initial non-successful shard state
        if self.store_shard_meta:
            self._store_shard_meta(False)

    def _store_shard_meta(self, successful: bool) -> None:
        # Store current shard success state (to distinguish successful / failed conversions without having to load zarr file)
        with open(
            self.output_dir_path
            / f"shard-meta-{self.sequence_id}-{util.padded_index_string(self.shard_id, index_digits=4)}.json",
            "w",
        ) as outfile:
            json.dump({"shard-id": self.shard_id, "shard-count": self.shard_count, "successful": successful}, outfile)

    # To be called after all data was added
    def finalize(self) -> Path:
        """Closes container and returns its path (optionally also writes shard meta data if requested)"""

        # Make sure the shard file is consolidated
        stores.consolidate_compressed_metadata(self.container_store)

        # Finish writing all files
        self.container_store.close()

        # Mark shard as successful
        if self.store_shard_meta:
            self._store_shard_meta(True)

        return self.output_container_path

    # Individual 'store*' methods performing data sanity checks and serialize consistent output formats
    def store_poses(self, poses: types.Poses) -> None:
        poses_grp = self.container_root.require_group("poses")
        poses_grp.create_dataset("T_rig_world_base", data=poses.T_rig_world_base)
        poses_grp.create_dataset("T_rig_worlds", data=poses.T_rig_worlds)
        poses_grp.create_dataset("T_rig_world_timestamps_us", data=poses.T_rig_world_timestamps_us)

    def store_tracks(self, tracks: types.Tracks) -> None:
        # Store track labels
        tracks_grp = self.container_root.require_group("tracks")
        tracks_grp.create_dataset(
            "track_labels",
            dtype=object,
            # Encode as array of (key,value) pairs because of
            # https://github.com/zarr-developers/numcodecs/pull/366
            data=[(k, v.to_dict()) for (k, v) in tracks.track_labels.items()],
            object_codec=numcodecs.JSON(),
        )

    def store_camera_meta(
        self,
        camera_id: str,
        # all frame timestamps
        frame_timestamps_us: np.ndarray,
        # extrinsics
        T_sensor_rig: np.ndarray,
        # intrinsics
        camera_model_parameters: types.ConcreteCameraModelParametersUnion,
        # sensor constants
        mask_image: Optional[PILImage.Image],
        # generic sensor meta-data (needs to be json-serializable)
        generic_meta_data: Dict[str, JsonLike],
    ) -> None:
        assert T_sensor_rig.shape == (4, 4)
        assert T_sensor_rig.dtype == np.dtype("float32")
        assert frame_timestamps_us.ndim == 1
        assert frame_timestamps_us.dtype == np.dtype("uint64")

        # Store meta data
        (camera_grp := self.container_root[SENSORS_BASE_GROUP][CAMERAS_BASE_GROUP][camera_id]).attrs.put(
            {
                "T_sensor_rig": T_sensor_rig.tolist(),
                "camera_model_type": camera_model_parameters.type(),
                "camera_model_parameters": camera_model_parameters.to_dict(),
                "generic_meta_data": generic_meta_data,
            }
        )

        # Store timestamps
        camera_grp.create_dataset("frame_timestamps_us", data=frame_timestamps_us)

        # Store mask if available
        if mask_image:
            with io.BytesIO() as buffer:
                FORMAT = "png"
                mask_image.save(buffer, format=FORMAT, optimize=True)  # encodes as png
                camera_grp.create_dataset("mask", data=np.asarray(buffer.getvalue()), compressor=None).attrs[
                    "format"
                ] = FORMAT

    def store_camera_frame(
        self,
        # data indexing
        camera_id: str,
        continuous_frame_index: int,
        # image data
        image_file_binary_data: bytes,
        image_file_format: str,
        # poses
        T_rig_worlds: np.ndarray,
        timestamps_us: np.ndarray,
        # generic per-frame data (key-value pairs, *not* dimension / dtype validated) and meta-data
        generic_data: Dict[str, np.ndarray],
        generic_meta_data: Dict[str, JsonLike],
    ) -> None:
        # sanity / consistency checks
        assert T_rig_worlds.shape == (2, 4, 4)
        assert T_rig_worlds.dtype == np.dtype("float32")
        assert timestamps_us.shape == (2,)
        assert timestamps_us.dtype == np.dtype("uint64")

        # Initialize frame
        continuous_frame_index_string = util.padded_index_string(continuous_frame_index)
        frame_group = self.container_root[SENSORS_BASE_GROUP][CAMERAS_BASE_GROUP][camera_id].require_group(
            continuous_frame_index_string
        )

        # Store image data
        frame_group.create_dataset("image", data=np.asarray(image_file_binary_data), compressor=None).attrs[
            "format"
        ] = image_file_format

        # Store pose data
        frame_group.create_dataset("T_rig_worlds", data=T_rig_worlds)
        frame_group.create_dataset("timestamps_us", data=timestamps_us)

        # Store additional generic frame data and meta-data (not dimension / dtype checked)
        (frame_generic_data_group := frame_group.require_group("generic_data")).attrs.put(generic_meta_data)
        for name, value in generic_data.items():
            frame_generic_data_group.create_dataset(name, data=value)

